public class ExchangeType {

        private int dia;
        private float compra;
        private float venta;
        public ExchangeType() {
        }
        public int getDia() {
            return dia;
        }
        public void setDia(int dia) {
            this.dia = dia;
        }
        public float getCompra() {
            return compra;
        }
        public void setCompra(float compra) {
            this.compra = compra;
        }
        public float getVenta() {
            return venta;
        }
        public void setVenta(float venta) {
            this.venta = venta;
        }
    }

